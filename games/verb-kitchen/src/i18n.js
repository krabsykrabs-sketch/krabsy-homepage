const STRINGS = { en: EN, de: DE, es: ES };

export const LANG = (() => {
  try {
    const p = new URLSearchParams(location.search).get('lang');
    if (p && STRINGS[p]) { localStorage.setItem(KEY, p); return p; }
    const s = localStorage.getItem(KEY);
    if (s && STRINGS[s]) return s;
  } catch (e) {}
  const nav = ((navigator.language || 'en').slice(0, 2)).toLowerCase();
  return STRINGS[nav] ? nav : 'en';
})();

/** Translate a key; template keys take args. Falls back to English. */
export function t(key, ...args) {
  const v = STRINGS[LANG][key] ?? EN[key];
  return typeof v === 'function' ? v(...args) : v;
}

/** Dish name for order tickets — falls back to the English name in recipes.js. */
export function dishName(id, fallback) {
  return STRINGS[LANG]['dish.' + id] || fallback;
}

/** Per-level recipe-card title/text — falls back to levels.js (image kept). */
export function levelTut(level) {
  const base = level.tutorial || {};
  const loc = STRINGS[LANG]['lvTut.' + level.id];
  return loc ? { ...base, ...loc } : base;
}

/** Swap every static data-i18n node once at boot. */
export function applyStatic() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const v = t(el.dataset.i18n);
    if (v != null) el.innerHTML = v;
  });
  const ml = document.getElementById('missedList');
  if (ml) ml.dataset.empty = t('perfectWash');   // the "Perfect washing!" ::after
  document.documentElement.lang = LANG;
}
